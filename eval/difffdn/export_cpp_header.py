#!/usr/bin/env python3
"""Export DiffFDN JSON presets into an embeddable C++ header."""

from __future__ import annotations

import argparse
import json
import math
from pathlib import Path
from typing import Dict, List, Tuple


def _parse_args() -> argparse.Namespace:
    parser = argparse.ArgumentParser(description=__doc__)
    parser.add_argument(
        "--preset",
        action="append",
        dest="presets",
        required=True,
        help="Path to a preset JSON (repeat --preset for multiple files)",
    )
    parser.add_argument(
        "--out-header",
        default="plugins/tiny-fdn-reverb/DiffFdnPresets.hpp",
        help="Output C++ header path",
    )
    return parser.parse_args()


def _to_float4(values: List[float], key: str) -> List[float]:
    if len(values) != 4:
        raise ValueError(f"Expected {key} to have 4 values, got {len(values)}")
    return [float(v) for v in values]


def _to_int4(values: List[int], key: str) -> List[int]:
    if len(values) != 4:
        raise ValueError(f"Expected {key} to have 4 values, got {len(values)}")
    return [int(v) for v in values]


def _normalize(v: List[float]) -> List[float]:
    norm = math.sqrt(sum(x * x for x in v))
    if norm < 1e-12:
        return [0.5, 0.5, 0.5, 0.5]
    return [x / norm for x in v]


def _infer_delay_set(config_id: str, delay_samples: List[int]) -> Tuple[str, int]:
    cid = config_id.lower()
    if "prime" in cid:
        return ("Prime", 0)
    if "spread" in cid:
        return ("Spread", 1)

    if delay_samples == [1499, 2377, 3217, 4421]:
        return ("Prime", 0)
    if delay_samples == [1200, 1800, 2400, 3000]:
        return ("Spread", 1)

    return ("Custom", -1)


def _fmt_f(v: float) -> str:
    return f"{float(v):.9g}f"


def _fmt_i4(values: List[int]) -> str:
    return "{ " + ", ".join(str(int(v)) for v in values) + " }"


def _fmt_f4(values: List[float]) -> str:
    return "{ " + ", ".join(_fmt_f(float(v)) for v in values) + " }"


def _sanitize_id(text: str) -> str:
    return "".join(ch if (ch.isalnum() or ch in "_-.") else "_" for ch in text)


def _load_preset(path: Path) -> Dict[str, object]:
    payload = json.loads(path.read_text())
    config_id = str(payload.get("config_id", path.stem))
    delay_samples = _to_int4(payload["delay_samples"], "delay_samples")  # type: ignore[index]
    gains = _to_float4(payload["gains"], "gains")  # type: ignore[index]
    u = _normalize(_to_float4(payload["u"], "u"))  # type: ignore[index]
    b = _to_float4(payload["b"], "b")  # type: ignore[index]
    c_l = _to_float4(payload["cL"], "cL")  # type: ignore[index]
    c_r = _to_float4(payload["cR"], "cR")  # type: ignore[index]
    delay_name, delay_id = _infer_delay_set(config_id, delay_samples)

    return {
        "config_id": config_id,
        "sr": int(payload["sr"]),
        "rt60": float(payload["rt60"]),
        "delay_set_name": delay_name,
        "delay_set_id": int(delay_id),
        "delay_samples": delay_samples,
        "gains": gains,
        "u_unit": u,
        "b": b,
        "cL": c_l,
        "cR": c_r,
        "source": str(path),
    }


def _render_header(presets: List[Dict[str, object]]) -> str:
    lines: List[str] = []
    lines.append("// Auto-generated by eval/difffdn/export_cpp_header.py")
    lines.append("// Do not edit by hand.")
    lines.append("#ifndef TINY_FDN_REVERB_DIFF_FDN_PRESETS_HPP")
    lines.append("#define TINY_FDN_REVERB_DIFF_FDN_PRESETS_HPP")
    lines.append("")
    lines.append("#include <cstddef>")
    lines.append("#include <cmath>")
    lines.append("")
    lines.append("namespace DISTRHO {")
    lines.append("")
    lines.append("struct DiffPreset {")
    lines.append("    const char* configId;")
    lines.append("    int sr;")
    lines.append("    const char* delaySetName;")
    lines.append("    int delaySetId;")
    lines.append("    float rt60;")
    lines.append("    int delay_samples[4];")
    lines.append("    float gains[4];")
    lines.append("    float u_unit[4];")
    lines.append("    float b[4];")
    lines.append("    float cL[4];")
    lines.append("    float cR[4];")
    lines.append("};")
    lines.append("")
    lines.append(f"constexpr std::size_t kDiffPresetCount = {len(presets)}u;")
    lines.append("constexpr DiffPreset kDiffPresets[kDiffPresetCount] = {")

    for preset in presets:
        lines.append("    {")
        lines.append(f"        \"{_sanitize_id(str(preset['config_id']))}\",")
        lines.append(f"        {int(preset['sr'])},")
        lines.append(f"        \"{preset['delay_set_name']}\",")
        lines.append(f"        {int(preset['delay_set_id'])},")
        lines.append(f"        {_fmt_f(float(preset['rt60']))},")
        lines.append(f"        {_fmt_i4(preset['delay_samples'])},")  # type: ignore[arg-type]
        lines.append(f"        {_fmt_f4(preset['gains'])},")  # type: ignore[arg-type]
        lines.append(f"        {_fmt_f4(preset['u_unit'])},")  # type: ignore[arg-type]
        lines.append(f"        {_fmt_f4(preset['b'])},")  # type: ignore[arg-type]
        lines.append(f"        {_fmt_f4(preset['cL'])},")  # type: ignore[arg-type]
        lines.append(f"        {_fmt_f4(preset['cR'])},")  # type: ignore[arg-type]
        lines.append("    },")

    lines.append("};")
    lines.append("")
    lines.append("inline const DiffPreset* findDiffPreset(int sr, int delaySetId, float rt60Approx) noexcept")
    lines.append("{")
    lines.append("    const DiffPreset* best = nullptr;")
    lines.append("    float bestDist = 0.0f;")
    lines.append("")
    lines.append("    for (std::size_t i = 0; i < kDiffPresetCount; ++i) {")
    lines.append("        const DiffPreset& p = kDiffPresets[i];")
    lines.append("        if (p.sr != sr)")
    lines.append("            continue;")
    lines.append("        if (delaySetId >= 0 && p.delaySetId != delaySetId)")
    lines.append("            continue;")
    lines.append("")
    lines.append("        const float dist = std::fabs(p.rt60 - rt60Approx);")
    lines.append("        if (best == nullptr || dist < bestDist) {")
    lines.append("            best = &p;")
    lines.append("            bestDist = dist;")
    lines.append("        }")
    lines.append("    }")
    lines.append("")
    lines.append("    return best;")
    lines.append("}")
    lines.append("")
    lines.append("} // namespace DISTRHO")
    lines.append("")
    lines.append("#endif // TINY_FDN_REVERB_DIFF_FDN_PRESETS_HPP")
    lines.append("")
    return "\n".join(lines)


def main() -> None:
    args = _parse_args()

    preset_paths = [Path(p) for p in args.presets]
    loaded = [_load_preset(path) for path in preset_paths]
    loaded.sort(key=lambda p: (int(p["sr"]), int(p["delay_set_id"]), float(p["rt60"]), str(p["config_id"])))

    out_header = Path(args.out_header)
    out_header.parent.mkdir(parents=True, exist_ok=True)
    out_header.write_text(_render_header(loaded))

    print(f"Wrote header: {out_header}")
    for p in loaded:
        print(
            f"  - {p['config_id']} sr={p['sr']} delaySet={p['delay_set_name']} "
            f"rt60={p['rt60']:.3f} source={p['source']}"
        )


if __name__ == "__main__":
    main()
